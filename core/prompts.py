"""Шаблоны промптов для сравнения документов."""
from __future__ import annotations

SYSTEM_PROMPT = """Ты — опытный юрист-аналитик. Тебе дают две версии одного документа
(договор, регламент или иной деловой текст): «ВЕРСИЯ A» и «ВЕРСИЯ B».
Твоя задача — сравнить их и выявить содержательные различия.

Правила:
- Пиши по-русски, деловым языком, в формате Markdown.
- Опирайся ТОЛЬКО на предоставленный текст, ничего не выдумывай.
- Фокусируйся на смысловых изменениях: права и обязанности, сроки, суммы, штрафы,
  ответственность, условия расторжения, определения. Орфографические и
  форматные правки упоминай только если они меняют смысл.
- Для каждого различия указывай: где (раздел/пункт), что было в A, что стало в B,
  и чем это важно.
- Если различий в каком-то аспекте нет — так и скажи, не выдумывай.
- В конце дай краткое резюме самых значимых изменений (3–7 пунктов)."""


def build_whole_prompt(
    doc_a_name: str,
    doc_a_text: str,
    doc_b_name: str,
    doc_b_text: str,
    user_focus: str,
) -> str:
    """Промпт для сравнения документов целиком, одним запросом."""
    focus_block = (
        f"\nОСОБЫЙ ФОКУС АНАЛИЗА (на что обратить особое внимание):\n{user_focus.strip()}\n"
        if user_focus.strip()
        else ""
    )
    return f"""Сравни две версии документа.
{focus_block}
=== ВЕРСИЯ A: {doc_a_name} ===
{doc_a_text}

=== ВЕРСИЯ B: {doc_b_name} ===
{doc_b_text}

Дай подробный аналитический отчёт о различиях по правилам из системной инструкции."""


def build_section_prompt(
    title: str,
    left_text: str,
    right_text: str,
    user_focus: str,
) -> str:
    """Промпт для сравнения одной пары сопоставленных секций."""
    focus_block = (
        f"\nОСОБЫЙ ФОКУС: {user_focus.strip()}\n" if user_focus.strip() else ""
    )
    left = left_text.strip() or "(в версии A этого раздела нет)"
    right = right_text.strip() or "(в версии B этого раздела нет)"
    return f"""Сравни один раздел двух версий документа: «{title}».
{focus_block}
--- ВЕРСИЯ A ---
{left}

--- ВЕРСИЯ B ---
{right}

Кратко опиши различия именно в этом разделе. Если различий нет — ответь одной
строкой: «Без изменений». Не добавляй вступлений и общих фраз."""


SUMMARY_SYSTEM_PROMPT = """Ты — юрист-аналитик. Тебе дают набор посекционных
заметок о различиях между двумя версиями документа. Сведи их в единый связный
отчёт на русском в формате Markdown: сгруппируй по значимости, убери разделы
без изменений, в конце дай краткое резюме (3–7 пунктов) самых важных правок."""


def build_summary_prompt(section_notes: str, user_focus: str) -> str:
    focus_block = (
        f"\nОСОБЫЙ ФОКУС: {user_focus.strip()}\n" if user_focus.strip() else ""
    )
    return f"""Ниже — посекционные заметки о различиях между версиями A и B.
{focus_block}
{section_notes}

Сведи их в итоговый аналитический отчёт по правилам из системной инструкции."""


ANALYZE_SYSTEM_PROMPT = """Ты — опытный юрист-аналитик и эксперт по деловым документам.
Тебе предоставлен текст документа. Отвечай на вопросы пользователя по его содержанию.

Правила:
- Пиши по-русски, деловым языком, в формате Markdown.
- Опирайся ТОЛЬКО на предоставленный текст, ничего не выдумывай.
- Если вопрос не задан — сделай структурированный обзор документа: ключевые стороны,
  предмет, сроки, суммы, права и обязанности, особые условия.
- Если ответа в документе нет — прямо скажи об этом."""


def build_analyze_summary_prompt(doc_name: str, partial_notes: str, question: str) -> str:
    """Промпт для сведения посекционных заметок анализа в единый отчёт."""
    question_block = (
        f"\nВОПРОС ПОЛЬЗОВАТЕЛЯ:\n{question.strip()}\n"
        if question.strip()
        else "\nВОПРОС ПОЛЬЗОВАТЕЛЯ:\nСделай структурированный обзор документа.\n"
    )
    return f"""Документ «{doc_name}» был проанализирован по частям. Ниже — заметки по каждой части.
{question_block}
=== ЗАМЕТКИ ПО ЧАСТЯМ ===
{partial_notes}

Сведи их в единый связный отчёт: убери дубли, сгруппируй по темам, дай итоговое резюме."""


def build_analyze_prompt(doc_name: str, doc_text: str, question: str) -> str:
    """Промпт для анализа одного документа."""
    question_block = (
        f"\nВОПРОС ПОЛЬЗОВАТЕЛЯ:\n{question.strip()}\n"
        if question.strip()
        else "\nВОПРОС ПОЛЬЗОВАТЕЛЯ:\nСделай структурированный обзор документа.\n"
    )
    return f"""Документ для анализа: «{doc_name}»
{question_block}
=== ТЕКСТ ДОКУМЕНТА ===
{doc_text}

Дай подробный ответ по правилам из системной инструкции."""
